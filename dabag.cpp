#include<iostream>
using namespace std;
int main(){
    int n;
    cout<<"Enter a number:";
    cin>>n;
    int i=0;
    while (i<=n)
    {
        int count=n-i+1;
        int j=1;
        while(j=0){
            cout<<count;
            ++count;
            ++j;
        }
        cout<<endl;
        ++i;
    }
    

}